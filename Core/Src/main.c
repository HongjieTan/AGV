	data1 = speed1;
	data2 = speed2;
	
	canConfigG[0].dataBuf[0] = 0x23;
	canConfigG[0].dataBuf[1] = 0x00;
	canConfigG[0].dataBuf[2] = 0x20;
	canConfigG[0].dataBuf[3] = 0x01;
	canConfigG[0].dataBuf[4] = (data1 & 0x00ff);
	canConfigG[0].dataBuf[5] = ((data1 >> 8) & 0x00ff);
	canConfigG[0].dataBuf[6] = 0x00;
